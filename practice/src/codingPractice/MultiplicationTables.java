package codingPractice;

public class MultiplicationTables {

   
    public static void main(String[] args)
    {
      int num =5;
      for(int i=1;i<=num;i++)
      {
          for(int j=1;j<=10;j++)
          {
              System.out.print(i+"*"+j+"="+i*j+"\t");
          }System.out.println();
      } 
     

    }

}
